/**
 * Returns the nearest mock building within ~200m of lat/lng, or null.
 */
export function findNearestMockBuilding(lat: number, lng: number): MockBuilding | null {
  const THRESHOLD_DEG = 0.002;

  let closest: MockBuilding | null = null;
  let minDist = Infinity;

  for (const b of MOCK_BUILDINGS) {
    const dLat = b.coordinates.lat - lat;
    const dLng = b.coordinates.lng - lng;
    const dist = Math.sqrt(dLat * dLat + dLng * dLng);
    if (dist < minDist && dist < THRESHOLD_DEG) {
      minDist = dist;
      closest = b;
    }
  }

  return closest;
}
